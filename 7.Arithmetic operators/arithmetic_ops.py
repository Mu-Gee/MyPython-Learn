# Arithmetic operators
print(10+3)
print(10-3)
print(10*3)

# Gives a floating point number
print(10/3)

# Gives a whole integer number
print(10//3)

# Gives remainder of the division
print(10 % 3)

# This is the exponent operator
print(10**3)

# Incrementing/decrementing value
x = 10
x = x+3
print(x)

# Augmented assignment operator +=/-=
x = 10
x += 3
print(x)

# Operator precedence ()->(**)->(*/)->(+-)
x = 10 + 3 * 2 ** 2
y = (2+3) * 10 - 2
print(x)
print(y)
